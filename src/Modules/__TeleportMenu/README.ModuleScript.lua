--[[
	In order to create map teleports, create a folder in workspace called "MapTeleports".
	Inside of this folder, place parts; these parts will be used as the Map Teleport locations. The name of the brick will be used as the name
	of the entry in the UI.
	
	Example:
	If you wanted to make a map teleport to "Asia," you would place a part down at your entrance to "Asia", put it inside the MapTeleports folder,
	and then name that part "Asia."

]]
